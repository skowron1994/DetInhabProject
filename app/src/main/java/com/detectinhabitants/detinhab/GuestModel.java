package com.detectinhabitants.detinhab;

public class GuestModel {
    private String gstName;
    private String gstSurname;
    private String gstStatus;
    private String gstActions;

    public String getGstName() {
        return gstName;
    }

    public void setGstName(String gstName) {
        this.gstName = gstName;
    }

    public String getGstSurname() {
        return gstSurname;
    }

    public void setGstSurname(String gstSurname) {
        this.gstSurname = gstSurname;
    }

    public String getGstStatus() {
        return gstStatus;
    }

    public void setGstStatus(String gstStatus) {
        this.gstStatus = gstStatus;
    }

    public String getGstActions() {
        return gstActions;
    }

    public void setGstActions(String gstActions) {
        this.gstActions = gstActions;
    }
}
